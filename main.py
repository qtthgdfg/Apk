class AndroidScreenshot:
    @staticmethod
    def capture():
        if CrossPlatformCompatibility.is_android():
            try:
                PythonActivity = autoclass('org.kivy.android.PythonActivity')
                activity = PythonActivity.mActivity
                view = activity.getWindow().getDecorView().getRootView()
                view.setDrawingCacheEnabled(True)
                bitmap = view.getDrawingCache()
                
                if bitmap:
                    ByteArrayOutputStream = autoclass('java.io.ByteArrayOutputStream')
                    Bitmap = autoclass('android.graphics.Bitmap')
                    stream = ByteArrayOutputStream()
                    bitmap.compress(Bitmap.CompressFormat.PNG, 100, stream)
                    image_data = stream.toByteArray()
                    view.setDrawingCacheEnabled(False)
                    return image_data
            except:
                pass
        elif CrossPlatformCompatibility.is_ios():
            try:
                from pyobjus import autoclass
                UIScreen = autoclass('UIScreen')
                UIGraphics = autoclass('UIGraphics')
                UIImage = autoclass('UIImage')
                
                screen = UIScreen.mainScreen()
                size = screen.bounds().size
                UIGraphicsBeginImageContext(size)
                context = UIGraphicsGetCurrentContext()
                
                for window in UIApplication.sharedApplication().windows():
                    window.layer().renderInContext_(context)
                
                image = UIGraphicsGetImageFromCurrentImageContext()
                UIGraphicsEndImageContext()
                
                return UIImagePNGRepresentation(image)
            except:
                pass
        return None

