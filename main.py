class AndroidStorage:
    @staticmethod
    def get_storage_info():
        info = {
            'internal': {
                'total': 0,
                'free': 0,
                'used': 0,
                'path': None
            },
            'external': [],
            'removable': []
        }
        
        if CrossPlatformCompatibility.is_android():
            try:
                Environment = autoclass('android.os.Environment')
                StatFs = autoclass('android.os.StatFs')
                
                internal_path = Environment.getDataDirectory().getPath()
                stat = StatFs(internal_path)
                
                if android_os.Build.VERSION.SDK_INT >= 18:
                    block_size = stat.getBlockSizeLong()
                    total_blocks = stat.getBlockCountLong()
                    free_blocks = stat.getAvailableBlocksLong()
                else:
                    block_size = stat.getBlockSize()
                    total_blocks = stat.getBlockCount()
                    free_blocks = stat.getAvailableBlocks()
                
                info['internal']['total'] = block_size * total_blocks
                info['internal']['free'] = block_size * free_blocks
                info['internal']['used'] = info['internal']['total'] - info['internal']['free']
                info['internal']['path'] = internal_path
                
                external_dirs = []
                if Environment.getExternalStorageDirectory():
                    external_dirs.append(Environment.getExternalStorageDirectory().getPath())
                
                if android_os.Build.VERSION.SDK_INT >= 19:
                    external_files_dirs = PythonActivity.mActivity.getExternalFilesDirs(None)
                    for dir in external_files_dirs:
                        if dir:
                            path = dir.getAbsolutePath()
                            if path not in external_dirs:
                                external_dirs.append(path)
                
                for path in external_dirs:
                    if path:
                        stat = StatFs(path)
                        if android_os.Build.VERSION.SDK_INT >= 18:
                            block_size = stat.getBlockSizeLong()
                            total_blocks = stat.getBlockCountLong()
                            free_blocks = stat.getAvailableBlocksLong()
                        else:
                            block_size = stat.getBlockSize()
                            total_blocks = stat.getBlockCount()
                            free_blocks = stat.getAvailableBlocks()
                        
                        storage = {
                            'total': block_size * total_blocks,
                            'free': block_size * free_blocks,
                            'used': block_size * total_blocks - block_size * free_blocks,
                            'path': path
                        }
                        
                        if path.startswith('/storage/emulated'):
                            info['external'].append(storage)
                        else:
                            info['removable'].append(storage)
                            
            except:
                pass
                
        return info

